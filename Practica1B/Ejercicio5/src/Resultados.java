
public class Resultados {
	private int Max = -99999;
	private int Min = 99999;
	private double Prom = 0;
	
	public int getMax() {
		return Max;
	}
	public void setMax(int max) {
		Max = max;
	}
	public int getMin() {
		return Min;
	}
	public void setMin(int min) {
		Min = min;
	}
	public double getProm() {
		return Prom;
	}
	public void setProm(double prom) {
		Prom = prom;
	}
	
}
